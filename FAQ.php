                  <div class="card-body">The donation process from the time you arrive until the time you leave takes the maximum of an hour. The donation itself is only about 8-10 minutes on average. The steps in the process are:<br><br>
                    <b>Registration (10 minutes)<br></b>
                    Donor fills up the registration form and gives his consent for donation.<br><br>

                    Medical Check up (5 Mins)<br>
                    Donors Medical History & lifestyle is asked, check up of temperature, blood pressure, pulse and haemoglobin<br><br>

                    <b>Donation (8 Mins) <br></b>
                    Phlebotomist draws a unit (350ml/450ml) of blood. A new sterile needle is used to draw each unit and the needle is destroyed after it has been used. It takes less than 10 min.<br>
                    An area on your arm will be cleansed and a brand–new, sterile needle will be inserted to draw the blood. This feels like a quick pinch and is over in seconds.<br>
                    You will have some time to relax while the bag is filling. (For a whole blood donation, it is about 8-10 minutes. If you are donating platelets, red cells or plasma by apheresis the collection can take up to 2 hours.)<br>
                    When approximately a pint of blood has been collected, the donation is complete and a staff person will place a bandage on your arm.<br><br>
                    <b>Refreshment (10 Mins)<br></b>
                    Enjoy Snacks and drink juice to replenish fluids.<br>
                    You will spend a few minutes enjoying refreshments to allow your body time to adjust to the slight decrease in fluid volume.<br>
                    After 10-15 minutes you can then leave the donation site and continue with your normal daily activities.<br>

