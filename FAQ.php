                    Five reasons why you should donate blood:<br><br>
                    <b>Development of new red blood cells<br></b>
                    Within the first 48 hours of blood donation, the donor's body starts replenishing the lost red blood cells. The replenishment process helps in staying healthy and productive at work.<br><br>
                    <b>Reducing risk of heart disease<br></b>
                    As per several studies and reports, when there is a rise in the iron level in blood, it raises the chances of heart diseases. People should donate blood from time to time in order to reduce the iron level in the blood.<br><br>
                    <b>Burns calories<br></b>
                    Donating blood can burn approximately 650 calories for each pint <br>
                    <b>Free blood test<br></b>
                    On donating blood, the donor receives a free mini health screening and blood tests.<br><br>
                    <b>Save lives<br></b>
                    Every time we donate one pint of blood it helps save three lives, so if you donate four times in a year you end up saving 12 lives. You don’t have to be a superhero to save someone, a simple act of donating blood can also save lives.<br><br>
